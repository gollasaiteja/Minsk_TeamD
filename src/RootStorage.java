public class QuadraticRoots {
 private double firstRoot;
 private double secondRoot;
 private boolean isImaginary;
 private boolean isQuadratic;
public boolean isQuadratic() {
	return isQuadratic;
}
public void setQuadratic(boolean isQuadratic) {
	this.isQuadratic = isQuadratic;
}
public boolean isImaginary() {
	return isImaginary;
}
public void setImaginary(boolean isImaginary) {
	this.isImaginary = isImaginary;
}
public double getFirstRoot() {
	return firstRoot;
}
public void setFirstRoot(double firstRoot) {
	this.firstRoot = firstRoot;
}
public double getSecondRoot() {
	return secondRoot;
}
public void setSecondRoot(double secondRoot) {
	this.secondRoot = secondRoot;
}
}
